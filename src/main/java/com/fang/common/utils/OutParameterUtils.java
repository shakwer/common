package com.fang.common.utils;

/**
 * <p>
 * Description: 输出参数模拟
 * </p>
 *
 * @author jinshilei
 *         2017年4月1日
 * @version 1.0
 * @param <T>
 *        泛型
 *
 */
public class OutParameterUtils<T> {

  /**
   * 引用值
   */
  private T value;

  /**
   *
   * getValue
   *
   * @return 返回值
   */
  public T getValue() {
    return value;
  }

  /**
   *
   * setValue
   *
   * @param value
   *        引用值
   */
  public void setValue(T value) {
    this.value = value;
  }
}
